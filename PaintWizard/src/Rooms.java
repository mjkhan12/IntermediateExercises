
public class Rooms {
		int wallNumber;
		int meter;
		
		public Rooms(int a, int b) {
			this.wallNumber = a; 
			this.meter = b;
		}

		public int getWallNumber() {
			return wallNumber;
		}

		public void setWallNumber(int wallNumber) {
			this.wallNumber = wallNumber;
		}

		public int getMeter() {
			return meter;
		}

		public void setMeter(int meter) {
			this.meter = meter;
		}
		
		
		
}
